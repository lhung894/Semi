    public void tableModelProduct(DefaultTableModel model) {
        Vector row;
        for (ProductDTO product : products) {
            row = new Vector();
            row.add(product.getProductId());
            row.add(product.getProductName());
            row.add(product.getProductQuantity());
            row.add(product.getProductDetail());
            model.addRow(row);
        }
    }

    public void initTableProduct(int role) {
        clear();
        setVisibleComponents(false);
        if (role != 1) {
            jBtnChucNang.setVisible(false);
            jBtnXoa.setVisible(false);
        }
        products = productBUS.getList();
        tags = tagBUS.getList();
        tbModelProduct.setRowCount(0);
        tableModelProduct(tbModelProduct);
        jTableProduct.setRowSorter(null);
        jTableProduct.setAutoCreateRowSorter(true);
        jTableProduct.setModel(tbModelProduct);
    }

    public void tableModelTag(DefaultTableModel model) {
        Vector row;
        for (TagDTO tag : tags) {
            if (tag.getProductId().equals(productId)) {
                row = new Vector();
                row.add(tag.getTagId());
                row.add(tag.getTagDateIn());
                row.add(tag.getTagGateIn());
                row.add(tag.getTagDateOut());
                row.add(tag.getTagGateOut());
                row.add(tag.getOrderId());
                model.addRow(row);
            }
        }
    }

    public void initTableTag() {
        tbModelTag.setRowCount(0);
        tableModelTag(tbModelTag);
        jTableTag.setModel(tbModelTag);
    }

    public String validation() {
        String validate = "";
        if (jTextPName.getText().equals("") || jTextPDetail.getText().equals("")) {
            validate += "Các trường thông tin không được bỏ trống!\n";
        }
        return validate;
    }

    public void themVector(DefaultTableModel model, ProductDTO productDTO) {
        Vector newrow = new Vector();
        newrow.add(productDTO.getProductId());
        newrow.add(productDTO.getProductName());
        newrow.add(productDTO.getProductQuantity());
        newrow.add(productDTO.getProductDetail());
        model.addRow(newrow);
    }

    public void suaVector(DefaultTableModel model, int row, ProductDTO productDTO) {
        model.setValueAt(productDTO.getProductName(), row, 1);
        model.setValueAt(productDTO.getProductDetail(), row, 3);
    }

    public void xoaVector(DefaultTableModel model, int row) {
        model.removeRow(row);
    }

