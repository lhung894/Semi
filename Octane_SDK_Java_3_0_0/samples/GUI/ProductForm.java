    private void jTableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductMouseClicked
        // TODO add your handling code here:
        int row = jTableProduct.getSelectedRow();
//        System.out.println("row: " + row);
        if (row == rowProduct) {
            jTableProduct.clearSelection();
            jBtnXoa.setEnabled(false);
            jBtnSua.setEnabled(false);
            jBtnThem.setEnabled(true);
            jBtnHuy.setEnabled(true);
            rowProduct = -2;
            productId = "";
            productName = "";
            jTextPName.setText("");
            jTextPDetail.setText("");
            tbModelTag.setRowCount(0);
            return;
        }
        if (row != -1) {
            rowProduct = row;
            productId = (String) jTableProduct.getValueAt(row, 0);
            productName = (String) jTableProduct.getValueAt(row, 1);
            jTextPName.setText(productName);
            jTextPDetail.setText((String) jTableProduct.getValueAt(row, 3));
            jBtnSua.setEnabled(true);
            jBtnThem.setEnabled(false);
            jBtnHuy.setEnabled(true);
            initTableTag();
            if (tbModelTag.getRowCount() == 0) {
                jBtnXoa.setEnabled(true);
            } else {
                jBtnXoa.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jTableProductMouseClicked

    private void jTableTagMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTagMouseClicked
        // TODO add your handling code here:
        int row = jTableTag.getSelectedRow();
        if (row == rowTag) {
            jTableTag.clearSelection();
            rowTag = -2;
            return;
        }
        if (row != -1) {
            rowTag = row;
        }
    }//GEN-LAST:event_jTableTagMouseClicked

    private void jBtnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnXoaActionPerformed
        // TODO add your handling code here:
        if (productId != null || !productId.equals("")) {
//            System.out.println("row count: " + tbModelTag.getRowCount());
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa sản phẩm?",
                    "Xác nhận", JOptionPane.OK_CANCEL_OPTION);
            if (confirm == JOptionPane.OK_OPTION) {
                if (productBUS.deleteProduct(productId)) {
                    xoaVector(tbModelProduct, rowProduct);
                    JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!");
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!");
                }
            }
        }
    }//GEN-LAST:event_jBtnXoaActionPerformed

    private void jBtnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnThemActionPerformed
        // TODO add your handling code here:
        String check = validation();
        if (check.equals("")) { //validation
            for (ProductDTO a : products) {
                if (a.getProductName().equals(jTextPName.getText())) {
                    JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại! Tên sản phẩm đã tồn tại!");
                    return;
                }
            }
            ProductDTO productDTO = new ProductDTO("", (String) jTextPName.getText(), 0, (String) jTextPDetail.getText());
            if (productBUS.insertProduct(productDTO)) {
                themVector(tbModelProduct, productDTO);
                products.add(productDTO);
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công!");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, check);
        }
    }//GEN-LAST:event_jBtnThemActionPerformed

    private void jBtnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSuaActionPerformed
        // TODO add your handling code here:
        String check = validation();
        if (check.equals("")) { //validation
            for (ProductDTO a : products) {
                if (a.getProductName().equals(jTextPName.getText()) && !a.getProductName().equals(productName)) {
                    JOptionPane.showMessageDialog(this, "Sửa sản phẩm thất bại! Tên sản phẩm đã tồn tại!");
                    return;
                }
            }
            ProductDTO productDTO = new ProductDTO((String) tbModelProduct.getValueAt(rowProduct, 0), (String) jTextPName.getText(), 0, (String) jTextPDetail.getText());
            if (productBUS.updateProduct(productDTO)) {
                suaVector(tbModelProduct, rowProduct, productDTO);
                products.set(rowProduct, productDTO);
                JOptionPane.showMessageDialog(this, "Sửa sản phẩm thành công!");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Sửa sản phẩm thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, check);
        }
    }//GEN-LAST:event_jBtnSuaActionPerformed

